#pragma once

#include <memory>

#include <GL/glew.h>

#include <graphics/GameObject.h>

namespace GE
{

	template <typename S>
	using sPtr = std::shared_ptr<S>;

	class Model : public GameObject
	{
		public:
			Model();
			~Model();
			void draw() const override;

		public:
			class ModelBuilder
			{
				public:
					ModelBuilder();
					~ModelBuilder() = default;
					sPtr<Model> create();
					ModelBuilder & addVertexBuffer(const float positions[],
					                               const uint vertexCount);
					ModelBuilder & addIndexBuffer(const uint indexes[],
					                              const uint indexesCount);

					void dataToVBO(const float data[]);
					void dataToInd(const uint data[]);

				private:
					sPtr<Model> m_model;
			};

		private:
			void createVAO();
			void createVBO();

		private:
			uint m_vaoId;
			uint m_vbos[2];
			uint m_vertexCount;
			uint m_indexesCount;
	};

} // GE
